import chisel3._
import chisel3.stage.{ChiselGeneratorAnnotation, ChiselStage}
import mem.SDRAMIO

/** This is the top-level module for the sound circuit. */
class Demo extends Module {
  val SDRAM_ADDR_WIDTH: Int = 13
  val SDRAM_DATA_WIDTH: Int = 16
  val BANK_WIDTH: Int = 2

  val io = IO(new Bundle {
    val led = Output(UInt(8.W))
    val sdram = new SDRAMIO(BANK_WIDTH, SDRAM_ADDR_WIDTH, SDRAM_DATA_WIDTH)
  })

  // Outputs
  io.led := 170.U
  io.sdram.cen := 0.U
  io.sdram.ras := 0.U
  io.sdram.cas := 0.U
  io.sdram.we := 0.U
  io.sdram.bank := 0.U
  io.sdram.addr := 0.U
  io.sdram.din := 0.U
}

object Demo extends App {
  (new ChiselStage).execute(
    Array("--compiler", "verilog", "--target-dir", "quartus/rtl", "--output-file", "ChiselTop"),
    Seq(ChiselGeneratorAnnotation(() => new Demo()))
  )
}
